    private void btnloginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnloginActionPerformed
        // TODO add your handling code here:
        try {
            String username=txtuser.getText();
            String password=txtpass.getText();
            String loginquery="select * from riderlogin where username = '"+username+"'and password='"+password+"'";
            Connection c=new Connection();
            ResultSet rs=c.s.executeQuery(loginquery);
            if(rs.next()){
                loggedInRiderId = rs.getInt("id");
                //JOptionPane.showMessageDialog(null, "rider id:"+loggedInRiderId);
                setVisible(false);
                RiderDashboard h=new RiderDashboard();
                h.setVisible(true);
                
            }
            else{
                JOptionPane.showMessageDialog(null, "Invalid Username or Password");
            }
        } 
        catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Login button exception:s"+e);
        }

    }//GEN-LAST:event_btnloginActionPerformed

